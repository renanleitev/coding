package tipos_var;

public class tipos_var{
    public static void main(String[] args) {
        int numero = 10;
        // Float = Colocar "f" no final (número pequeno)
        float decimalCurto = 2.5f;
        // Double = Pode usar sem o "f" (número grande)
        double decimalLongo = 11.5;
        // Char = Usar aspas simples (um caractere)
        char caractere = 'a';
        // String = Usar aspas duplas (múltiplos caracteres)
        String palavra = "casa";
        // Boolean = True or False
        boolean certoErrado = true;
        System.out.println("int: " + numero);
        System.out.println("float: " + decimalCurto);
        System.out.println("double " + decimalLongo);
        System.out.println("char: " + caractere);
        System.out.println("string: " + palavra);
        System.out.println("boolean: " + certoErrado);
    }
}