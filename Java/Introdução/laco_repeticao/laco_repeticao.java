package laco_repeticao;

public class laco_repeticao {
    public static void main(String args[]){
        int num01 = 0, num02 = 20;

        // Incrementando (não é preciso informar o número)
        while (num01 < num02){
            num01++;
            System.out.println(num01);
        }

        // Reduzindo (não é preciso informar o número)
        while (num01 > 0){
            num01--;
            System.out.println(num01);
        }
    }
}
