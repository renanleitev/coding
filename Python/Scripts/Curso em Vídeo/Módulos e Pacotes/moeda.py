    print('-'*30)
    print('     RESUMO DO VALOR')
    print('-' * 30)
    print(f'A metade de R$ {p} é R$ {p/2}')
    print(f'O dobro de R$ {p} é R$ {p*2}')
    print(f'O preço com 10% de aumento é R$ {p*1.1}')
    print(f'O preço com 10% de desconto é R$ {p*0.9}')
